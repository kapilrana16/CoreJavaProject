package OOPS;

public class MethodOverloading 
{
	
	public void show()
	{
		System.out.println("show 1");
	}
	
	public void show(int a)
	{
		System.out.println("show 1");
	}
	
	public void show(int a, String b)
	{
		System.out.println("show 1");
	}
	
	public void show(String a, int b)
	{
		System.out.println("show 1");
	}

	
	
	public static void main(String[] args) 
	{
		
	}

}
